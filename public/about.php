              <div class="panel-body">
                <p>The data on political financing can be found on the Electoral Commission of Malta’s website. The data set is incomplete; the figures declared do not match the extent of the donations and the data set only covers the period between 2016 and 2019, with a couple entries for 2020. Moreover, the information is provided in hand-written form by the party members and it is rubber-stamped with no attempt to verify the information provided or the source of the funds being donated. Indeed, party funding regulations are not properly enforced.</p>
                <p>In the ‘Donation Type’ diagram, the entries are categorized according to the amount of donations in that category, but not all individual donations are listed. For instance, the Partit Nazzjonalista received 135 donations between 500 and 7000 euros during Q4 of 2019, but this is only listed as 1 donation in the diagram.</p>
                <p>In the ‘Top Donors’ diagram, the vast majority of donations (<99%) are anonymous which skews the data.</p> 
                <p>Other statistics offered in the table at the bottom include:
                <ul>
                <li>The Parties</li>
                <li>The type of donation</li>
                <li>The time period (of 3 months)</li>
                <li>The year</li>
                <li>The total amount of money broken down by type of donation.</li>
                </ul></p>
