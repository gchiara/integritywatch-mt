                <li>How many directorships or associations MPs listed.</li>
                <li>Whether MPs listed any other financial interests.</li>
                </ul></p>
                <p>When you click on any of the segments in the diagrams, it shows you which MPs are being represented. For instance, if you click on the ‘10+’ section in the properties diagram, you will see a list of MPs who have listed more than 10 properties in their declarations, etc.</p> 
